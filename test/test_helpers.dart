File createTemporaryFile(String filename, [Uint8List? data]) {
  final dir = Directory.systemTemp.createTempSync();

  final tempFile = File('${dir.path}/$filename');
  tempFile.createSync();

  if (data != null) {
    tempFile.writeAsBytesSync(data);
  }

  return tempFile;
}

